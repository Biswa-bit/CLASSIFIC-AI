from core.base_dashboard import BaseDashboard


class DatasetDashboard(BaseDashboard):

    """
    Dataset Dashboard Builder
    """

    ##########################################################
    # Build Dashboard
    ##########################################################

    def build(self, summary):
        """
        Build a standardized dashboard object.

        Parameters
        ----------
        summary : dict
            Dataset summary generated by Dataset Agent.

        Returns
        -------
        dict
            Standardized dashboard object.
        """

        dashboard = {

            "title": "Dataset Dashboard",

            "metrics": {

                "Rows": summary["Rows"],

                "Columns": summary["Columns"],

                "Numeric Columns": summary["Numeric Columns"],

                "Categorical Columns": summary["Categorical Columns"],

                "Datetime Columns": summary["Datetime Columns"],

                "Duplicate Rows": summary["Duplicate Rows"],

                "Dataset Size (MB)": summary["Dataset Size (MB)"]

            }

        }

        return dashboard

    ##########################################################
    # Display Dashboard
    ##########################################################

    def show(self, summary):
        """
        Display Dataset Dashboard.
        """

        dashboard = self.build(summary)

        self.header(dashboard["title"])

        self.section("Dataset Information")

        for metric, value in dashboard["metrics"].items():

            print(f"{metric:<25}: {value}")

        self.footer()
        
        